package pe.igh.app.model;

public class TablaItem {

	private int num1;
	private int num2;
	private int rpta;
	private String operador;

	public TablaItem() {
	}

	public int getNum1() {
		return num1;
	}

	public void setNum1(int num1) {
		this.num1 = num1;
	}

	public int getNum2() {
		return num2;
	}

	public void setNum2(int num2) {
		this.num2 = num2;
	}

	public int getRpta() {
		return rpta;
	}

	public void setRpta(int rpta) {
		this.rpta = rpta;
	}

	public String getOperador() {
		return operador;
	}

	public void setOperador(String operador) {
		this.operador = operador;
	}

}
